# > FOR

# range = faixa de x a y, pulando de z em z -> for i in range(x, y, z)

print("\nImprimindo 'for variavel in range(10):' ")
 
for variavel in range(10):
    print(variavel)
    # vai do 0 até menor que 10, ou seja, de 0 até 9

print("\nImprimindo 'for variavel in range(0,10):' ")

for variavel in range(0,10):
    print(variavel)
    # vai do 0 até 9
    
