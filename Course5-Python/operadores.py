# > Operação Matemática (Aritméticas)   

"""
    - Soma: +
    - Subtração: -
    - Multiplicação: *
    - Divisão: /
    - Divisão inteira: //
    - Resto da divisão: %
    - Potência: **
"""

numberA = 10
numberB = 20

print(numberA + numberB)
print(numberA - numberB)
print(numberA * numberB)
print(numberA / numberB)
print(numberA // numberB) # 10 / 20 = 0 e resto 10 PORTANTO considera o quociente
print(numberA % numberB) # 10 / 20 = 0 e resto 10 PORTANTO considera o resto
print(numberA ** numberB) # potência

