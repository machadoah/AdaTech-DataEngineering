nome = "Antonio Henrique"

print("Seu nome é",nome)

"""
    Tipos de variáveis:
    
    1. int: números inteiros, i.e. números sem parte decimal obrigatória na sua representação. (e.g. 0, -5, -1)
    
    2. float: numeros reais, i.e. números com parte decimal. (e.g. 1.0, -1.4)
    
    3. str: cadeia de caracteres, i.e. dados textuais.
    
    4. bool: valores booleanos, i.e. True ou False
"""

estudando = True

print("O tipo da variavel estudando é",type(estudando))