# > FUNÇÕES DE LISTAS

lista = [18, 18, 19, 21, 23, 34, 40, 23, 38, 21, 17, 18, 19, 20, 24, 27, 30, 27, 20, 24]

# len (devolve o tamanho da lista)

print(f"A lista possui {len(lista)} elementos")

# sum

print(f"A somatória dos elementos da lista resulta em {sum(lista)}")

# max

print(f"O menor elemento da lista é {max(lista)}")

# min

print(f"O menor elemento da lista é {min(lista)}")